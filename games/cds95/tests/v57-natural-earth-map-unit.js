'use strict';
const assert=require('node:assert/strict');
const fs=require('node:fs');
const path=require('node:path');
const root=path.join(__dirname,'..');
const html=fs.readFileSync(path.join(root,'public/index.html'),'utf8');
const teacher=fs.readFileSync(path.join(root,'public/teacher.html'),'utf8');
const dir=path.join(root,'public/assets/maps/natural-earth/tiles');
const tiles=fs.readdirSync(dir).filter(f=>/^tile_\d+_\d+\.jpg$/.test(f));
const manifest=JSON.parse(fs.readFileSync(path.join(root,'public/assets/maps/natural-earth/manifest.json'),'utf8'));
assert.deepEqual(manifest,{width:16200,height:8100,tileSize:512,cols:32,rows:16,format:'jpg'});
assert.equal(tiles.length,512);
assert.ok(tiles.every(f=>fs.statSync(path.join(dir,f)).size>1000));
assert.match(html,/NATURAL_MAP_TILE_CACHE_LIMIT=72/);
assert.match(html,/function naturalMapTile\(col,row\)/);
assert.match(html,/function drawNaturalEarthLayer\(cw,ch,left,top\)/);
assert.match(html,/tile_\$\{col\}_\$\{row\}\.jpg\?v=57/);
assert.match(teacher,/natural-earth\/overview\.jpg\?v=57/);
assert.doesNotMatch(html,/ocean_original_tiles/);
console.log(JSON.stringify({ok:true,source:[16200,8100],tiles:tiles.length,lazyLoad:true}));
