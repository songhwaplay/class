'use strict';
const assert=require('node:assert/strict');
const fs=require('node:fs');
const path=require('node:path');
const root=path.join(__dirname,'..');
const html=fs.readFileSync(path.join(root,'public/index.html'),'utf8');
const overview=path.join(root,'public/assets/maps/natural-earth/overview.jpg');
assert.ok(fs.existsSync(overview));
assert.ok(fs.statSync(overview).size>100_000);
assert.match(html,/natural-earth\/overview\.jpg\?v=57/);
assert.doesNotMatch(html,/ocean_original_tiles|biome_palette_mask|paletteAlpha|paletteMix/);
console.log('Natural Earth direct-color rendering ok');
